def unique(l):
    lst=[]
    for i in l:
        if i not in lst:
            lst.append(i)
    return lst

l=[1,2,3,3,3,4,5]
a=unique(l)
print(a)

print("-----------------------------------------------------------------------------------------------------------------------------------------------------------------------")



